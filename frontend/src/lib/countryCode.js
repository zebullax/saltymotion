/**
 * Translate an ISO country code to its usual name
 * @param {string} cCode
 * @return {string} Name
 */
export const resolveCountryCode = (cCode) => {
  return isoCountries[cCode.toUpperCase()].name;
};

/**
 * Return the char code for the country flag from the country code
 * @param {string} isoCode
 * @return {string}
 */
export function countryToFlag(isoCode) {
  return typeof String.fromCodePoint !== "undefined"
    ? isoCode.toUpperCase().replace(/./g, (char) => String.fromCodePoint(char.charCodeAt(0) + 127397))
    : isoCode.toUpperCase();
}
